document.addEventListener('DOMContentLoaded', () => {
  const langSwitcher = document.getElementById('custom-lang-switcher');
  const langBtn = document.getElementById('lang-switcher-btn');
  const langText = document.getElementById('lang-switcher-text');
  const langDropdown = document.getElementById('lang-switcher-dropdown');

  if (!langSwitcher) return;

  const savedLang = localStorage.getItem('ahlan_lang') || 'en';
  
  const updateDropdownUI = (lang) => {
    const options = langDropdown.querySelectorAll('li');
    options.forEach(opt => {
      opt.classList.remove('active');
      if (opt.dataset.value === lang) {
        opt.classList.add('active');
        langText.textContent = opt.textContent;
      }
    });
  };

  updateDropdownUI(savedLang);
  applyLanguage(savedLang);

  // Toggle Dropdown
  langBtn.addEventListener('click', (e) => {
    e.stopPropagation();
    langSwitcher.classList.toggle('open');
    langBtn.setAttribute('aria-expanded', langSwitcher.classList.contains('open'));
  });

  // Handle Selection
  langDropdown.querySelectorAll('li').forEach(item => {
    item.addEventListener('click', () => {
      const lang = item.dataset.value;
      localStorage.setItem('ahlan_lang', lang);
      updateDropdownUI(lang);
      applyLanguage(lang);
      langSwitcher.classList.remove('open');
      langBtn.setAttribute('aria-expanded', 'false');
    });
  });

  // Close when clicking outside
  document.addEventListener('click', (e) => {
    if (!langSwitcher.contains(e.target)) {
      langSwitcher.classList.remove('open');
      langBtn.setAttribute('aria-expanded', 'false');
    }
  });

  function applyLanguage(lang) {
    document.documentElement.lang = lang;
    if (lang === 'ar') {
      document.documentElement.dir = 'rtl';
    } else {
      document.documentElement.dir = 'ltr';
    }

    const t = translations[lang];
    if (!t) return;

    for (const [selector, config] of Object.entries(elementMapping)) {
      const el = document.querySelector(selector);
      if (!el) continue;
      
      let key, isHTML, keepSVG;
      if (typeof config === 'string') {
        key = config;
      } else {
        key = config.key;
        isHTML = config.isHTML;
        keepSVG = config.keepSVG;
      }

      if (!t[key]) continue;

      if (keepSVG) {
        const svg = el.querySelector('svg');
        if (svg) {
          el.innerHTML = '';
          el.appendChild(svg);
          el.appendChild(document.createTextNode(' ' + t[key]));
        } else {
          el.textContent = t[key];
        }
      } else if (isHTML) {
        el.innerHTML = t[key];
      } else {
        el.textContent = t[key];
      }
    }
    
    // Placeholder updates
    const nameInput = document.getElementById('name');
    if(nameInput) nameInput.placeholder = lang === 'ar' ? 'الاسم الكامل' : (lang === 'fr' ? 'Nom Complet' : 'Jane Smith');
    
    const companyInput = document.getElementById('company');
    if(companyInput) companyInput.placeholder = lang === 'ar' ? 'الشركة' : (lang === 'fr' ? 'Acme Corp' : 'Acme Corp');

    const emailInput = document.getElementById('email');
    if(emailInput) emailInput.placeholder = lang === 'ar' ? 'البريد الإلكتروني' : (lang === 'fr' ? 'jane@acme.com' : 'jane@acme.com');

    const msgInput = document.getElementById('message');
    if(msgInput) msgInput.placeholder = lang === 'ar' ? 'أخبرنا عن احتياجاتك...' : (lang === 'fr' ? 'Parlez-nous de vos besoins...' : 'Tell us about your travel program needs...');
    
    const fromInput = document.getElementById('s-from');
    if(fromInput) fromInput.placeholder = lang === 'ar' ? 'المدينة أو المطار' : (lang === 'fr' ? 'Ville ou aéroport' : 'City or airport');
    
    const toInput = document.getElementById('s-to');
    if(toInput) toInput.placeholder = lang === 'ar' ? 'المدينة أو المطار' : (lang === 'fr' ? 'Ville ou aéroport' : 'City or airport');
  }
});
